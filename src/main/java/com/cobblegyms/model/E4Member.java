public class E4Member {
    public String uuid;
    public String name;
    public String type1;
    public String type2;
    public int wins;
    public int losses;
    public E4Member(String uuid, String name, String type1, String type2, int wins, int losses) {
        this.uuid = uuid;
        this.name = name;
        this.type1 = type1;
        this.type2 = type2;
        this.wins = wins;
        this.losses = losses;
    }
}